/**
 * Load theme stylesheets
 */
function load_styles($hook) {
    wp_enqueue_style( 'theme-stylesheet', get_stylesheet_uri(), [], get_theme_version() );
}
add_action('wp_enqueue_scripts', 'load_styles');


